bool upload(char *input, uint16_t fb_len, uint16_t waitingTime) {
  bool result = false;
  Serial.println("Connect to " + String(myDomain));
  WiFiClientSecure client;
  //WiFiClient client;
  //client.setCACert(test_root_ca);
  
  if (client.connect(myDomain, 443)) {
    Serial.println("Connection successful");
    
    char output[base64_enc_len(3)];
    String imageFile = "";
    for (int i=0;i<fb_len;i++) {
      base64_encode(output, (input++), 3);
      if (i%3==0) imageFile += urlencode(String(output));
    }
    String Data = myFilename+mimeType+myImage;
    
    Serial.println("POST " + myScript + " HTTP/1.1");
    Serial.println("Host: " + String(myDomain));
    Serial.println("Content-Length: " + String(Data.length()+imageFile.length()));
    Serial.println("Content-Type: application/x-www-form-urlencoded");
    Serial.println();
    Serial.println(Data);

    
    client.println("POST " + myScript + " HTTP/1.1");
    client.println("Host: " + String(myDomain));
    client.println("Content-Length: " + String(Data.length()+imageFile.length()));
    client.println("Content-Type: application/x-www-form-urlencoded");
    client.println();
    client.print(Data);
    int Index;
    for (Index = 0; Index < imageFile.length(); Index = Index+1000) {
      client.print(imageFile.substring(Index, Index+1000));
      Serial.println(imageFile.substring(Index, Index+1000));
    }
    
    Serial.println("Waiting for response.");
    long int StartTime=millis();
    while (!client.available()) {
      Serial.print(".");
      delay(100);
      if ((StartTime+waitingTime) < millis()) {
        Serial.println();
        Serial.println("No response.");
        //If you have no response, maybe need a greater value of waitingTime
        break;
      }
    }
    Serial.println();   
    while (client.available()) {
      Serial.print(char(client.read()));
    }
    result = true;  
  } else {         
    Serial.println("Connected to " + String(myDomain) + " failed.");
  }
  client.stop();
  return result;
}
